/**
 * 현재 path에 맞는 도움말 반환 (경로 시작 일치 또는 /report/* 통일)
 */
export function getHelpForPath(path) {
  if (!path || path === "/") return null;
  // 보고서 작성: /report, /report/123 등
  if (path.startsWith("/report")) return helpByPath["/reportForm"];
  return helpByPath[path] ?? null;
}
